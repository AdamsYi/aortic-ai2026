import { createHash } from "node:crypto";
import { mkdir, readFile, readdir, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const defaultCaseRoot = path.join(repoRoot, "cases/default_clinical_case");
const distDefaultCaseRoot = path.join(repoRoot, "dist/default-case");
const outputPath = path.join(repoRoot, "src/generated/defaultCaseBundle.ts");

function sha256Hex(input) {
  return createHash("sha256").update(input).digest("hex");
}

function extractConst(source, name) {
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const match = source.match(new RegExp(`export const ${escaped} = "([^"]*)";`));
  return match ? match[1] : null;
}

async function readDirMap(dirPath, mode) {
  const entries = await readdir(dirPath, { withFileTypes: true });
  const files = entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .sort();
  const out = {};
  for (const name of files) {
    const absPath = path.join(dirPath, name);
    const raw = await readFile(absPath);
    out[name] = mode === "utf8" ? raw.toString("utf8") : raw.toString("base64");
  }
  return out;
}

async function writeDirMap(dirPath, map, mode) {
  await rm(dirPath, { recursive: true, force: true });
  await mkdir(dirPath, { recursive: true });
  for (const [name, value] of Object.entries(map)) {
    const absPath = path.join(dirPath, name);
    if (mode === "utf8") {
      await writeFile(absPath, value, "utf8");
    } else {
      await writeFile(absPath, Buffer.from(value, "base64"));
    }
  }
}

export async function buildDefaultCaseBundle({ buildVersion } = {}) {
  if (!buildVersion || typeof buildVersion !== "string") {
    throw new Error("default_case_bundle_requires_build_version");
  }

  const artifacts = await readDirMap(path.join(defaultCaseRoot, "artifacts"), "utf8");
  const qa = await readDirMap(path.join(defaultCaseRoot, "qa"), "utf8");
  const meshes = await readDirMap(path.join(defaultCaseRoot, "meshes"), "base64");
  const reports = await readDirMap(path.join(defaultCaseRoot, "reports"), "base64");
  const imaging = await readDirMap(path.join(defaultCaseRoot, "imaging_hidden"), "base64");
  const manifest = JSON.parse(artifacts["case_manifest.json"] || "{}");
  manifest.build_version = buildVersion;
  artifacts["case_manifest.json"] = JSON.stringify(manifest, null, 2);

  await writeDirMap(path.join(distDefaultCaseRoot, "artifacts"), artifacts, "utf8");
  await writeDirMap(path.join(distDefaultCaseRoot, "qa"), qa, "utf8");
  await writeDirMap(path.join(distDefaultCaseRoot, "meshes"), meshes, "base64");
  await writeDirMap(path.join(distDefaultCaseRoot, "reports"), reports, "base64");
  await writeDirMap(path.join(distDefaultCaseRoot, "imaging_hidden"), imaging, "base64");

  const digests = {};
  for (const [groupName, group] of Object.entries({ artifacts, qa, meshes, reports, imaging })) {
    for (const [name, value] of Object.entries(group)) {
      digests[`${groupName}/${name}`] = sha256Hex(value);
    }
  }
  const defaultCaseDigest = sha256Hex(
    JSON.stringify({
      buildVersion,
      manifest,
      digests,
    })
  );
  digests["default_case_digest"] = defaultCaseDigest;

  let existingOutput = "";
  try {
    existingOutput = await readFile(outputPath, "utf8");
  } catch {
    existingOutput = "";
  }
  const previousBuildVersion = extractConst(existingOutput, "DEFAULT_CASE_BUILD_VERSION");
  const previousDigest = extractConst(existingOutput, "DEFAULT_CASE_DIGEST");
  if (existingOutput && previousBuildVersion === buildVersion && previousDigest && previousDigest !== defaultCaseDigest) {
    throw new Error(
      `default_case_build_version_reuse_detected:${buildVersion}:${previousDigest}:${defaultCaseDigest}`
    );
  }

  const moduleSource = `// Auto-generated by scripts/build_default_case_bundle.mjs
export const DEFAULT_CASE_BUILD_VERSION = ${JSON.stringify(buildVersion)};
export const DEFAULT_CASE_DIGEST = ${JSON.stringify(defaultCaseDigest)};
export const DEFAULT_CASE_FILE_DIGESTS = ${JSON.stringify(digests, null, 2)};
export const DEFAULT_CASE_ASSET_PREFIX = "/default-case";
`;

  await writeFile(outputPath, moduleSource, "utf8");
  return {
    buildVersion,
    defaultCaseDigest,
    digests,
    outputPath,
  };
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const buildVersion = process.argv[2];
  buildDefaultCaseBundle({ buildVersion }).then(({ outputPath: generated }) => {
    console.log(`Generated ${path.relative(repoRoot, generated)} (${buildVersion})`);
  });
}
